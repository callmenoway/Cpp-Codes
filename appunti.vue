Giovanni Boccaccio nasce nel 1313, in quella data Petrarca 9 anni e Dante quasi 50 (da lì a poco la sua morte a Firenze).
Nel corso del 300 il fallimento del progetto imperiale, nel quale aveva creduto Dante. Lo spostamento della sede papale in Francia,
il consolidamento dei regni di Francia ed Inghilterra ed infine la peste nera che devastò il continente (1340-1350).
Boccaccio è figlio illegittimo di un mercante fiorentino, nasce in Toscana (dubbia provenienza tra Firenze o Certaldo).
Boccaccio all'inizio della sua vita seguì il padre che inizialmente lavorava presso la compagnia dei bardi a Firenze e
successivamente il padre fu nominato rappresentante  dei bardi presso gli Angioini a Napoli (tra le città più importanti).
Boccaccio naturalmente lo seguì e imparò dal padre il mestiere di mercante, entrando anche in contatto con nobili e intellettuali
della corte di Roberto. La corte Angioina  era un dinamico centro culturale nel quale Boccaccio potè imparare la letteratura 
cortese e cavalleresca, sopratutto in francese, ed i classici latini.
Negli anni napoletani Boccaccio conobbe anche Fiammetta, la donna che ispirò il suo primo capolavoro, il Filocolo. 
Nel 1340 Boccaccio tornò a Firenze, dove si dedicò alla letteratura e alla poesia, ma anche alla vita mondana e alle donne ma,
nel 1348, la peste nera lo costrinse a rifugiarsi a Certaldo, dove scrisse il Decameron.
Tra il 1350 ed il 1360 Boccaccio si dedicò all'aiuto del comune di Firenze, sopratutto si dedicò al culto di Dante, risarcendo
alla figlia il denaro per l'esilio del padre. 
